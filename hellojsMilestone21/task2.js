/**
 * Take a number if the number is odd multiply it by 2 and return the result. 
 * If the number is even divide it by two and return the result.
 */function oddEvenOperation(num) {
    if (num % 2 !== 0) {
        // odd number multiply by
    return num;        
    }else{
        //even number divide by 2 
        return num / 2;
    }
    
 }
 console.log(oddEvenOperation(5));
 console.log(oddEvenOperation(8));
 